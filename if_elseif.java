class if_elseif {
    public static void main(String[] args) {
        int a = 4;
        if (a == 4) {
            System.out.println("a is equal to 4");
        } else if (a == 5) {
            System.out.println("a is equal to 5");
        } else {
            System.out.println("a is diffrent");
        }
    }
}